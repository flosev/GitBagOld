DROP TABLE Users IF EXISTS ;
DROP TABLE Fashions IF EXISTS ;
DROP TABLE Commodities IF EXISTS;


CREATE TABLE Fashions (
  id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  caption varchar(64) NOT NULL
 );

CREATE TABLE Users (
  id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  login varchar(255) /*UNIQUE*/ NOT NULL,
  password varchar(255) NOT NULL,
  email varchar(255) /*UNIQUE*/ NOT NULL
);

CREATE TABLE Commodities (
  id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  article varchar(255) /*UNIQUE*/ NOT NULL,
  length INTEGER NOT NULL ,
  width INTEGER NOT NULL ,
  heigth INTEGER NOT NULL ,
  price INTEGER /*UNIQUE*/ NOT NULL,
  fashion_id INTEGER /*UNIQUE*/ NOT NULL,
  FOREIGN KEY (fashion_id)
  REFERENCES Fashions(id)
)




